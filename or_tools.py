def return_solution(data, manager, routing, assignment, prt):
    """return assignment on console."""
    # print(f'Objective: {assignment.ObjectiveValue()}')
    # Display dropped nodes.
    dropped_nodes = 'Dropped nodes:'
    dropped_nodes_count = 0
    for node in range(routing.Size()):
        if routing.IsStart(node) or routing.IsEnd(node):
            continue
        if assignment.Value(routing.NextVar(node)) == node:
            dropped_nodes += ' {}'.format(manager.IndexToNode(node))
            dropped_nodes_count += 1
    # print(dropped_nodes)
    if prt:
        print(f'Dropped num = {dropped_nodes_count}')
    # Display routes
    total_distance = 0
    total_load = 0
    sol = []
    for vehicle_id in range(data['num_vehicles']):
        route = []
        index = routing.Start(vehicle_id)
        plan_output = 'Route for vehicle {}:\n'.format(vehicle_id)
        route_distance = 0
        route_load = 0
        while not routing.IsEnd(index):
            node_index = manager.IndexToNode(index)
            if node_index != 0:
                route.append(node_index)
            service_time = data['service_times'][node_index]
            route_load += data['demands'][node_index]
            plan_output += ' {0} Load({1}) -> '.format(node_index, route_load)
            previous_index = index
            index = assignment.Value(routing.NextVar(index))
            route_distance += routing.GetArcCostForVehicle(
                previous_index, index, vehicle_id)
            # remove service time
            route_distance -= service_time
        plan_output += ' {0} Load({1})\n'.format(manager.IndexToNode(index),
                                                 route_load)
        plan_output += 'Distance of the route: {}m\n'.format(route_distance)
        plan_output += 'Load of the route: {}\n'.format(route_load)
        if route_load > 0:
            # print(plan_output)
            sol.append(route)
        total_distance += route_distance
        total_load += route_load
    # print('Total Distance of all routes: {}m'.format(total_distance))
    return sol
