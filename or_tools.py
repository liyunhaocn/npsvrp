def or_main(data,run_time = 10, prt = 0):
    """Solve the CVRP problem."""
    # Instantiate the data problem.
    # data = create_data_model()
    data['num_vehicles'] = 300
    data['vehicle_capacities'] = [data['capacity'] for i in range(data['num_vehicles'])]
    data['depot'] = 0
    # Create the routing index manager.
    manager = pywrapcp.RoutingIndexManager(len(data['coords']),
                                           data['num_vehicles'], data['depot'])

    # Create Routing Model.
    routing = pywrapcp.RoutingModel(manager)


    # Create and register a transit callback.
    def distance_callback(from_index, to_index):
        """Returns the distance between the two nodes."""
        # Convert from routing variable Index to distance matrix NodeIndex.
        from_node = manager.IndexToNode(from_index)
        to_node = manager.IndexToNode(to_index)
        return data['duration_matrix'][from_node][to_node] + data['service_times'][from_node]

    transit_callback_index = routing.RegisterTransitCallback(distance_callback)

    # Define cost of each arc.
    routing.SetArcCostEvaluatorOfAllVehicles(transit_callback_index)

    # Add Time Windows constraint.
    time = 'Time'
    routing.AddDimension(
        transit_callback_index,
        10000000,  # allow waiting time
        10000000,  # maximum time per vehicle
        False,  # Don't force start cumul to zero.
        time)
    time_dimension = routing.GetDimensionOrDie(time)
    # Add time window constraints for each location except depot.
    for location_idx, time_window in enumerate(data['time_windows']):
        if location_idx == data['depot']:
            continue
        index = manager.NodeToIndex(location_idx)
        time_dimension.CumulVar(index).SetRange(int(time_window[0]), int(time_window[1]))

    # Add Capacity constraint.
    def demand_callback(from_index):
        """Returns the demand of the node."""
        # Convert from routing variable Index to demands NodeIndex.
        from_node = manager.IndexToNode(from_index)
        return data['demands'][from_node]

    demand_callback_index = routing.RegisterUnaryTransitCallback(
        demand_callback)
    routing.AddDimensionWithVehicleCapacity(
        demand_callback_index,
        0,  # null capacity slack
        data['vehicle_capacities'],  # vehicle maximum capacities
        True,  # start cumul to zero
        'Capacity')
    # Allow to drop nodes.
    # penalty = 100000
    for node in range(1, len(data['demands'])):
        a = int(data['penalty'][manager.NodeToIndex(node)]) + int(data['service_times'][manager.NodeToIndex(node)])
        routing.AddDisjunction([manager.NodeToIndex(node)], a)

    # initial_solution = routing.ReadAssignmentFromRoutes(data['initial_routes'],
    #                                                     True)
    # Setting first solution heuristic.
    search_parameters = pywrapcp.DefaultRoutingSearchParameters()
    #
    # search_parameters = pywrapcp.RoutingModel.DefaultSearchParameters()
    search_parameters.first_solution_strategy = (
        routing_enums_pb2.FirstSolutionStrategy.PATH_CHEAPEST_ARC)
    search_parameters.local_search_metaheuristic = (
        routing_enums_pb2.LocalSearchMetaheuristic.GUIDED_LOCAL_SEARCH)
    search_parameters.time_limit.FromSeconds(int(run_time))
    # iter number
    search_parameters.solution_limit = 10000

    # Solve the problem.
    # solution = routing.SolveFromAssignmentWithParameters(
    #     initial_solution, search_parameters)

    # Solve the problem.
    assignment = routing.SolveWithParameters(search_parameters)

    sol = []
    # Print solution on console.
    if assignment:
        # print_solution(data, manager, routing, assignment)
        sol = return_solution(data, manager, routing, assignment,prt)
    return sol


